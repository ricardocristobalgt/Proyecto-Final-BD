CREATE OR REPLACE PACKAGE PKG_SEGUIMIENTO AS
    FUNCTION DOSIS_PENDIENTES(P_ID_PACIENTE NUMBER) RETURN NUMBER;
    PROCEDURE GENERAR_DOSIS_TRATAMIENTO(P_ID_TRATAMIENTO NUMBER);
    PROCEDURE GENERAR_ALERTAS_ATRASADAS;
END PKG_SEGUIMIENTO;
/

CREATE OR REPLACE PACKAGE BODY PKG_SEGUIMIENTO AS

    FUNCTION DOSIS_PENDIENTES(P_ID_PACIENTE NUMBER) RETURN NUMBER IS
        V_TOTAL NUMBER;
    BEGIN
        SELECT COUNT(*)
        INTO V_TOTAL
        FROM DOSIS_PROGRAMADAS
        WHERE ID_PACIENTE = P_ID_PACIENTE
          AND ESTADO = 'PENDIENTE';

        RETURN V_TOTAL;
    END DOSIS_PENDIENTES;

    PROCEDURE GENERAR_DOSIS_TRATAMIENTO(P_ID_TRATAMIENTO NUMBER) IS
        V_ID_PACIENTE NUMBER;
        V_FECHA_INICIO DATE;
        V_FECHA_FIN DATE;
        V_FECHA_ACTUAL DATE;
        V_FECHA_DOSIS TIMESTAMP;
        V_HORAS NUMBER;
        V_DIAS NUMBER;
        V_ITER NUMBER;
        V_TOTAL_DOSIS NUMBER;
    BEGIN
        SELECT ID_PACIENTE, FECHA_INICIO, FECHA_FIN
        INTO V_ID_PACIENTE, V_FECHA_INICIO, V_FECHA_FIN
        FROM TRATAMIENTOS
        WHERE ID_TRATAMIENTO = P_ID_TRATAMIENTO;

        FOR D IN (
            SELECT ID_TRATAMIENTO_DETALLE, FRECUENCIA_HORAS, HORA_INICIO, DURACION_DIAS
            FROM TRATAMIENTO_DETALLE
            WHERE ID_TRATAMIENTO = P_ID_TRATAMIENTO
        ) LOOP
            V_HORAS := D.FRECUENCIA_HORAS;
            V_DIAS := D.DURACION_DIAS;
            V_TOTAL_DOSIS := FLOOR((V_DIAS * 24) / V_HORAS);
            V_ITER := 0;

            WHILE V_ITER < V_TOTAL_DOSIS LOOP
                V_FECHA_DOSIS :=
                    TO_TIMESTAMP(
                        TO_CHAR(V_FECHA_INICIO, 'YYYY-MM-DD') || ' ' || NVL(D.HORA_INICIO, '08:00'),
                        'YYYY-MM-DD HH24:MI'
                    ) + NUMTODSINTERVAL(V_ITER * V_HORAS, 'HOUR');

                IF CAST(V_FECHA_DOSIS AS DATE) <= V_FECHA_FIN + 1 THEN
                    INSERT INTO DOSIS_PROGRAMADAS (
                        ID_DOSIS,
                        ID_TRATAMIENTO_DETALLE,
                        ID_PACIENTE,
                        FECHA_PROGRAMADA,
                        ESTADO
                    ) VALUES (
                        SEQ_MED_DOSIS.NEXTVAL,
                        D.ID_TRATAMIENTO_DETALLE,
                        V_ID_PACIENTE,
                        V_FECHA_DOSIS,
                        'PENDIENTE'
                    );
                END IF;

                V_ITER := V_ITER + 1;
            END LOOP;
        END LOOP;
    END GENERAR_DOSIS_TRATAMIENTO;

    PROCEDURE GENERAR_ALERTAS_ATRASADAS IS
    BEGIN
        FOR R IN (
            SELECT 
                DP.ID_DOSIS,
                DP.ID_PACIENTE,
                P.ID_DOCTOR,
                M.NOMBRE AS MEDICAMENTO,
                DP.FECHA_PROGRAMADA
            FROM DOSIS_PROGRAMADAS DP
            INNER JOIN PACIENTES P ON P.ID_PACIENTE = DP.ID_PACIENTE
            INNER JOIN TRATAMIENTO_DETALLE TD ON TD.ID_TRATAMIENTO_DETALLE = DP.ID_TRATAMIENTO_DETALLE
            INNER JOIN MEDICAMENTOS M ON M.ID_MEDICAMENTO = TD.ID_MEDICAMENTO
            WHERE DP.ESTADO = 'PENDIENTE'
              AND DP.FECHA_PROGRAMADA < CURRENT_TIMESTAMP - INTERVAL '2' HOUR
              AND NOT EXISTS (
                    SELECT 1 FROM ALERTAS A
                    WHERE A.ID_DOSIS = DP.ID_DOSIS
                      AND A.TIPO_ALERTA = 'DOSIS_NO_REPORTADA'
              )
        ) LOOP
            UPDATE DOSIS_PROGRAMADAS
            SET ESTADO = 'ATRASADA'
            WHERE ID_DOSIS = R.ID_DOSIS;

            INSERT INTO ALERTAS (
                ID_ALERTA,
                ID_PACIENTE,
                ID_DOCTOR,
                ID_DOSIS,
                TIPO_ALERTA,
                MENSAJE,
                ESTADO
            ) VALUES (
                SEQ_MED_ALERTAS.NEXTVAL,
                R.ID_PACIENTE,
                R.ID_DOCTOR,
                R.ID_DOSIS,
                'DOSIS_NO_REPORTADA',
                'El paciente no reportó la dosis programada del medicamento ' || R.MEDICAMENTO,
                'PENDIENTE'
            );
        END LOOP;
    END GENERAR_ALERTAS_ATRASADAS;

END PKG_SEGUIMIENTO;
