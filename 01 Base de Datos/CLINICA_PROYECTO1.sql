CREATE OR REPLACE VIEW VW_PACIENTES_RESUMEN AS
SELECT 
    P.ID_PACIENTE,
    P.NOMBRES || ' ' || P.APELLIDOS AS PACIENTE,
    NVL(D.NOMBRES || ' ' || D.APELLIDOS, 'Sin doctor') AS DOCTOR,
    U.USERNAME,
    P.TELEFONO,
    P.ESTADO,
    P.FECHA_REGISTRO
FROM PACIENTES P
INNER JOIN USUARIOS U ON U.ID_USUARIO = P.ID_USUARIO
LEFT JOIN DOCTORES D ON D.ID_DOCTOR = P.ID_DOCTOR;

CREATE OR REPLACE VIEW VW_DOSIS_PENDIENTES AS
SELECT
    DP.ID_DOSIS,
    DP.ID_PACIENTE,
    P.NOMBRES || ' ' || P.APELLIDOS AS PACIENTE,
    M.NOMBRE AS MEDICAMENTO,
    TD.DOSIS,
    DP.FECHA_PROGRAMADA,
    DP.ESTADO
FROM DOSIS_PROGRAMADAS DP
INNER JOIN PACIENTES P ON P.ID_PACIENTE = DP.ID_PACIENTE
INNER JOIN TRATAMIENTO_DETALLE TD ON TD.ID_TRATAMIENTO_DETALLE = DP.ID_TRATAMIENTO_DETALLE
INNER JOIN MEDICAMENTOS M ON M.ID_MEDICAMENTO = TD.ID_MEDICAMENTO
WHERE DP.ESTADO = 'PENDIENTE';

CREATE OR REPLACE VIEW VW_ALERTAS_DOCTOR AS
SELECT
    A.ID_ALERTA,
    A.ID_DOCTOR,
    A.ID_PACIENTE,
    P.NOMBRES || ' ' || P.APELLIDOS AS PACIENTE,
    A.TIPO_ALERTA,
    A.MENSAJE,
    A.FECHA_ALERTA,
    A.ESTADO
FROM ALERTAS A
INNER JOIN PACIENTES P ON P.ID_PACIENTE = A.ID_PACIENTE;

