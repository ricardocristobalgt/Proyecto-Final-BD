BEGIN
    FOR t IN (
        SELECT table_name FROM user_tables
        WHERE table_name IN (
            'ALERTAS','REGISTRO_PRESION','REGISTRO_GLUCOSA','SEGUIMIENTO_DOSIS',
            'DOSIS_PROGRAMADAS','TRATAMIENTO_DETALLE','TRATAMIENTOS','MEDICAMENTOS',
            'DIAGNOSTICOS','CONSULTAS','PACIENTES','ENFERMERAS','DOCTORES',
            'TIPOS_PRESION','TIPOS_DIABETES','USUARIOS','ROLES'
        )
    ) LOOP
        EXECUTE IMMEDIATE 'DROP TABLE ' || t.table_name || ' CASCADE CONSTRAINTS';
    END LOOP;

    FOR s IN (
        SELECT sequence_name FROM user_sequences
        WHERE sequence_name LIKE 'SEQ_MED_%'
    ) LOOP
        EXECUTE IMMEDIATE 'DROP SEQUENCE ' || s.sequence_name;
    END LOOP;
EXCEPTION
    WHEN OTHERS THEN NULL;
END;
