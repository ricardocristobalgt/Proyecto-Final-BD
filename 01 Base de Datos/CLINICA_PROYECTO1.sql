CREATE OR REPLACE PACKAGE BODY PKG_REPORTES AS

    FUNCTION TOTAL_PACIENTES_DOCTOR(P_ID_DOCTOR NUMBER) RETURN NUMBER IS
        V_TOTAL NUMBER;
    BEGIN
        SELECT COUNT(*)
        INTO V_TOTAL
        FROM PACIENTES
        WHERE ID_DOCTOR = P_ID_DOCTOR
          AND ESTADO = 'ACTIVO';

        RETURN V_TOTAL;
    END TOTAL_PACIENTES_DOCTOR;

    FUNCTION TOTAL_ALERTAS_PENDIENTES(P_ID_DOCTOR NUMBER) RETURN NUMBER IS
        V_TOTAL NUMBER;
    BEGIN
        SELECT COUNT(*)
        INTO V_TOTAL
        FROM ALERTAS
        WHERE ID_DOCTOR = P_ID_DOCTOR
          AND ESTADO = 'PENDIENTE';

        RETURN V_TOTAL;
    END TOTAL_ALERTAS_PENDIENTES;

END PKG_REPORTES;
